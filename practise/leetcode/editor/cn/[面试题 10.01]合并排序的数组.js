/**
 * @param {number[]} A
 * @param {number} m
 * @param {number[]} B
 * @param {number} n
 * @return {void} Do not return anything, modify A in-place instead.
 */
var merge = function(A, m, B, n) {
    let k = m + n - 1;
    let j = m - 1;
    let i = n - 1;

    while (i >= 0 && j >= 0) {
        if (A[i] >= B[j]) {
            A[k--] = A[i--];
        } else {
            A[k--] = B[j--];
        }
    }

    while (i >= 0) {
        A[k--] = A[i--];
    }

    while (j >= 0) {
        A[k--] = B[j--];
    }

};
